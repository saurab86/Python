#Indexing in string
variable = "Python String Course"

print(variable)
print(variable[4])   #Print "o" as string is initialized in index as var[0]
print(variable[6])   #Print blank space 

#Negative Indexing
print()
print(variable[-1])  #Print the last indexing value i.e 'e' from word 'course'
print(variable[-2])  #Print the second last indexing value i.e 's' from word 'course'

